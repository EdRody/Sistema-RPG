public class Feitico {
    String nome;
    int nivel;
    String descricao;

    public Feitico(String nome, int nivel, String descricao) {
        this.nome = nome;
        this.nivel = nivel;
        this.descricao = descricao;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }
}
